    <h1>Privacy Policy</h1>
    <p class="updated">Tactics Board apps · Last updated: May 29, 2026</p>

    <p>
        This Privacy Policy explains how the Tactics Board family of apps
        (the "App", "we", "us") handles your information. It applies to all of
        our sport-specific tactics board apps published on the App Store and
        Google Play. By using the App you agree to this policy.
    </p>

    <h2>Information We Collect</h2>
    <ul>
        <li>
            <strong>Account &amp; sign-in.</strong> If you choose to sign in
            with Apple or Google, we receive your name, email address, and a
            user identifier. This is used solely to create your account and to
            sync your content across your devices. Signing in is optional — the
            App works without an account.
        </li>
        <li>
            <strong>Your content (cloud sync).</strong> If you enable cloud
            sync, the tactics boards, formations, and related data you create
            are stored on our servers so they are available on your other
            devices.
        </li>
        <li>
            <strong>Photos.</strong> If you add a photo as a player avatar, the
            App accesses the photo you select from your library or camera. These
            images stay on your device and are included in your synced content
            only if you enable cloud sync.
        </li>
        <li>
            <strong>Support requests.</strong> If you contact us through the
            in-app form, we receive the email address and message you provide so
            we can respond.
        </li>
        <li>
            <strong>Advertising.</strong> Some of our apps display ads through
            Google AdMob. To serve and measure ads, AdMob and its partners may
            collect device identifiers and app-usage data. We do not show the
            App Tracking Transparency prompt, so on iOS ads are served on a
            non-personalized basis. See
            <a href="https://policies.google.com/technologies/partner-sites">How Google uses information</a>
            and the
            <a href="https://support.google.com/admob/answer/6128543">AdMob data
            disclosure</a> for details.
        </li>
    </ul>

    <h2>How We Use Information</h2>
    <ul>
        <li>To provide core features such as saving and syncing your boards.</li>
        <li>To respond to your support requests.</li>
        <li>To display and measure advertising (in apps that show ads).</li>
        <li>To maintain the security and reliability of the service.</li>
    </ul>

    <h2>Sharing</h2>
    <p>
        We do not sell your personal information. We share information only with
        service providers that help us operate the App — notably Google AdMob
